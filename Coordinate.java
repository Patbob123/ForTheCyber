/**
 * This stores an x and y
 * 
 * @author Dawson
 * @version nov 2023
 */
public class Coordinate  
{
    // instance variables
    private int x;
    private int y;
    /**
     * Constructor for objects of class Coordinate
     */
    public Coordinate(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
}
